
<p>Plugins are never required, but suggested. &mdash; Make <em>My Plugin Manager</em> your plugin manager, and give your users an easy experience for installing and managing suggested plugins.</p>

<h2 id="install" class="heading-sm">Installation Prompt</h2>

<p>Upon activating your theme or plugin containing your plugin manager drop-in, the user will be greeted with a simple message prompting them to install any suggested plugins which they haven't already installed.</p>

<p><?php image( '/screenshots/install-nag.png' ); ?></p>

<h2 id="plugin-manager" class="heading-sm">Plugin Manager</h2>

<p>After following the prompt to manage suggested plugins, the user is taken to your custom plugin manager interface.</p>

<p><?php image( '/screenshots/manager-init.png' ); ?></p>

<h2 id="single-actions" class="heading-sm">Single Actions</h2>

<p>For each suggested plugin, the user can install, activate, deactivate, delete, or even update any plugin with a single click.</p>

<p><?php image( '/screenshots/single-action.gif' ); ?></p>

<h2 id="bulk-actions" class="heading-sm">Bulk Actions</h2>

<p>And for an even faster experience, the user can perform all actions in bulk.</p>

<p><?php image( '/screenshots/bulk-action.gif' ); ?></p>

<h2 id="manage" class="heading-sm">Ongoing Management</h2>

<p>Your plugin manager interface is meant to serve a consistent experience, that remains in place no matter which of the suggested plugins have been installed. At any time, your users can get the satisfaction of a birds-eye view of all suggested plugins, with their status and current compatibility.</p>

<p><?php image( '/screenshots/manage.png' ); ?></p>

<h2 id="status" class="heading-sm">Plugin Status</h2>

<p>Suggested plugins can have one of four statuses &mdash; not installed, installed, active, or incompatible. Taking into account the screenshot below, let's assess the current status of the five plugins shown.</p>

<ol>
	<li><strong>Not Installed.</strong> The plugin hasn't been installed yet.</li>
	<li><strong>Installed.</strong> The user has installed the plugin, but they haven't activated it yet.</li>
	<li><strong>Active.</strong> The plugin is installed and active. It's also up-to-date and compatible.</li>
	<li><strong>Incompatible.</strong> The plugin's installed version is less than your plugin manager's minimum compatible version. A plugin can only be marked incompatible if it's active.</li>
	<li><strong>Active.</strong> The plugin is installed and active. While there is an update available from WordPress and an update link is presented to take action, its status remains as "Active" because the current version is still compatible with your product.</li>
</ol>

<p><?php image( '/screenshots/states.png' ); ?></p>

<h2 id="updates" class="heading-sm">Plugin Updates</h2>

<p>To continue developing a successful WordPress product for the long haul, it's important for users to know when any suggested plugins are no longer compatible with your updated product. User will be notified when any plugins are no longer compatible so they can quickly get everything updated and working smoothly.</p>

<p><?php image( '/screenshots/update.gif' ); ?></p>

<h2 id="external" class="heading-sm">External Plugins</h2>

<p>To give your users a more secure on-boarding experience, installing plugins from third-party sources directly is not supported. When one of these non-wordpress.org plugins is among the suggested plugins, a link will be provided to the user, "Get Plugin."</p>

<p><?php image( '/screenshots/external-install.png' ); ?></p>

<p>Once the plugin has been installed, its status can be managed the same as any other suggested plugin. And like most commercial plugins do, if they've included a system to filter into WordPress's update system, those updates will be reflected, as well.</p>

<p><?php image( '/screenshots/external-update.gif' ); ?></p>

